<template>
  <form @submit="addItem"> 
    <input type="text" placeholder="New To Do" 
    v-model="title" 
    @input="check">
    <input type="submit" value="add" class="add-btn">
  </form>
</template>

<script>
import { ref } from "vue";
// import {v4 as uuidv4} from 'uuid'

export default {
    name:'AddTodo',
    setup(props, context){
        const title =ref('')

        const addItem = function(event){
            event.preventDefault()//chống lại hành động mặc định action của html
           
            const newItem = {
            //id: null,
            title: title.value,
            completed: false
        }
        context.emit('add-todo', newItem)

        title.value=''
        
        }

        
    return { title,addItem }
    }
}
</script>

